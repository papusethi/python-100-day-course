# Data types
# a datatype represents the type of a data

# Strings
print("Hello")

# sub-string
print("Hello"[0])   # H
print("Hello"[1])   # e
print("Hello"[4])   # o

# Integer
print("123" + "456")    # this is not an integer, it's a string
print(123 + 345)

# in large numbers we are giving (,) to represent
# in python we can use (_) to show big numbers
# but python ignores the (_) and show as usual without(, or _)

# Floating Point
print(124.456)
print(123_456.98)
print(234_33.234_23)

# Boolean
print(True)
print(False)